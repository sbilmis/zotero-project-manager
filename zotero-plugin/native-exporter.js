/* Native, dependency-free Zotero workspace exporter. */

var ZPMNativeExporter = (() => {
  const CONTROL_DIRECTORY = ".zpm";
  const MANIFEST_VERSION = 4;
  const MANIFEST_FILENAME = "manifest.json";
  const GENERATED_MARKER = "<!-- Generated by zpm; changes will be replaced. -->";
  const ASSET_MARKER = "Generated by zpm; image assets may be replaced.\n";
  const DEFAULT_FILENAME_TEMPLATE = "author_year_title";
  const FILENAME_TEMPLATES = new Set([
    DEFAULT_FILENAME_TEMPLATE,
    "author_title_year", "year_author_title", "year_title_author",
    "title_author_year", "title_year_author", "author_title",
    "year_title", "title_author", "title_year", "title",
  ]);
  const ANNOTATION_LAYOUTS = new Set(["separate", "sidecar", "bundle"]);
  const RESERVED_WINDOWS_NAMES = new Set([
    "CON", "PRN", "AUX", "NUL",
    ...Array.from({ length: 9 }, (_, index) => `COM${index + 1}`),
    ...Array.from({ length: 9 }, (_, index) => `LPT${index + 1}`),
  ]);

  class ExportError extends Error {}

  function slash(value) {
    return String(value || "").replaceAll("\\", "/");
  }

  function relativeParts(value) {
    return slash(value).split("/").filter((part) => part && part !== ".");
  }

  function joinRelative(...values) {
    return values.flatMap(relativeParts).join("/");
  }

  function parentRelative(value) {
    const parts = relativeParts(value);
    parts.pop();
    return parts.join("/");
  }

  function basename(value) {
    return relativeParts(value).at(-1) || "";
  }

  function extension(value) {
    const name = basename(value);
    const index = name.lastIndexOf(".");
    return index > 0 ? name.slice(index) : "";
  }

  function stem(value) {
    const name = basename(value);
    const suffix = extension(name);
    return suffix ? name.slice(0, -suffix.length) : name;
  }

  function isSafeRelative(value) {
    const normalized = slash(value);
    return Boolean(normalized)
      && !normalized.startsWith("/")
      && !/^[A-Za-z]:\//.test(normalized)
      && !relativeParts(normalized).includes("..");
  }

  function sanitizeComponent(value, fallback = "Untitled", maxLength = 180) {
    let normalized = String(value || "").normalize("NFC")
      .replace(/[<>:"/\\|?*\u0000-\u001f]/g, "-")
      .replace(/\s+/g, " ")
      .replace(/^[ .]+|[ .]+$/g, "");
    if (!normalized) normalized = fallback;
    if (RESERVED_WINDOWS_NAMES.has(normalized.toUpperCase())) normalized = `_${normalized}`;
    if (normalized.length > maxLength) {
      normalized = normalized.slice(0, maxLength).replace(/[ .-]+$/g, "");
    }
    return normalized || fallback;
  }

  function extractYear(value) {
    return String(value || "").match(/(?:^|\D)(\d{4})(?!\d)/)?.[1] || null;
  }

  function validateFilenameTemplate(value) {
    const normalized = String(value || DEFAULT_FILENAME_TEMPLATE).trim().toLowerCase();
    if (!FILENAME_TEMPLATES.has(normalized)) {
      throw new ExportError(`Unknown filename template: ${value}`);
    }
    return normalized;
  }

  function validateAnnotationLayout(value) {
    const normalized = String(value || "separate").trim().toLowerCase();
    if (!ANNOTATION_LAYOUTS.has(normalized)) {
      throw new ExportError(`Unknown annotation layout: ${value}`);
    }
    return normalized;
  }

  function attachmentFilename(attachment, template = DEFAULT_FILENAME_TEMPLATE) {
    const source = attachment.source_path || attachment.original_path || "";
    const suffix = extension(source).toLowerCase() || ".bin";
    let title = attachment.title;
    if (title && suffix !== ".bin" && title.toLowerCase().endsWith(suffix)) {
      title = title.slice(0, -suffix.length).replace(/[ .]+$/g, "");
    }
    const values = {
      author: attachment.creators?.[0] || null,
      year: extractYear(attachment.date),
      title: title || stem(source) || attachment.item_key || "Untitled",
    };
    const parts = validateFilenameTemplate(template)
      .split("_")
      .map((field) => values[field])
      .filter(Boolean);
    return `${sanitizeComponent(parts.join(" - "))}${suffix}`;
  }

  function chooseAvailableName(desired, reserved, key = null) {
    let candidate = desired;
    if (!reserved.has(candidate.toLowerCase())) {
      reserved.add(candidate.toLowerCase());
      return candidate;
    }
    const suffix = extension(desired);
    const base = stem(desired);
    if (key) {
      candidate = `${base} [${sanitizeComponent(key, "item", 32)}]${suffix}`;
      if (!reserved.has(candidate.toLowerCase())) {
        reserved.add(candidate.toLowerCase());
        return candidate;
      }
    }
    for (let index = 2; ; index += 1) {
      candidate = `${base} (${index})${suffix}`;
      if (!reserved.has(candidate.toLowerCase())) {
        reserved.add(candidate.toLowerCase());
        return candidate;
      }
    }
  }

  function isPdf(attachment) {
    return String(attachment.content_type || "").toLowerCase() === "application/pdf"
      || String(attachment.original_path || "").toLowerCase().endsWith(".pdf")
      || String(attachment.source_path || "").toLowerCase().endsWith(".pdf");
  }

  function isoTimestamp() {
    return new Date().toISOString().replace(/\.\d{3}Z$/, "+00:00");
  }

  function identity(collectionKey, attachmentKey) {
    return `${collectionKey}\u0000${attachmentKey}`;
  }

  function quotePath(value) {
    return slash(value).split("/").map(encodeURIComponent).join("/");
  }

  function relativeLink(fromDirectory, target) {
    const from = relativeParts(fromDirectory);
    const to = relativeParts(target);
    while (from.length && to.length && from[0] === to[0]) {
      from.shift();
      to.shift();
    }
    return [...from.map(() => ".."), ...to].join("/") || ".";
  }

  function decodeHtml(value) {
    const entities = {
      amp: "&", lt: "<", gt: ">", quot: "\"", apos: "'", nbsp: " ",
    };
    return String(value || "").replace(/&(#x?[0-9a-f]+|[a-z]+);/gi, (match, token) => {
      if (token[0] === "#") {
        const hexadecimal = token[1]?.toLowerCase() === "x";
        const number = Number.parseInt(token.slice(hexadecimal ? 2 : 1), hexadecimal ? 16 : 10);
        return Number.isFinite(number) ? String.fromCodePoint(number) : match;
      }
      return entities[token.toLowerCase()] ?? match;
    });
  }

  function noteHtmlToMarkdown(value) {
    return decodeHtml(String(value || "")
      .replace(/<(script|style)\b[^>]*>[\s\S]*?<\/\1>/gi, "")
      .replace(/<br\s*\/?>/gi, "\n")
      .replace(/<li\b[^>]*>/gi, "\n- ")
      .replace(/<\/?(?:p|div|blockquote|h[1-6]|ul|ol)\b[^>]*>/gi, "\n")
      .replace(/<[^>]+>/g, ""))
      .split(/\r?\n/)
      .map((line) => line.replace(/[ \t]+/g, " ").trim())
      .join("\n")
      .trim()
      .replace(/\n{3,}/g, "\n\n");
  }

  function normalizeManifest(payload) {
    if (!payload || ![1, 2, 3, MANIFEST_VERSION].includes(Number(payload.version))) {
      return null;
    }
    const version = Number(payload.version);
    const items = Array.isArray(payload.items) ? payload.items.map((item) => ({
      ...item,
      source_sha256: item.source_sha256 ?? null,
      destination_size: Number(item.destination_size ?? item.source_size ?? 0),
      destination_mtime_ns: Number(item.destination_mtime_ns ?? item.source_mtime_ns ?? 0),
      state: item.state || "active",
    })) : [];
    return {
      version: MANIFEST_VERSION,
      exported_at: String(payload.exported_at || ""),
      collection_key: String(payload.collection_key || ""),
      collection_name: String(payload.collection_name || ""),
      filename_template: validateFilenameTemplate(payload.filename_template),
      annotation_layout: validateAnnotationLayout(payload.annotation_layout),
      items,
      legacy_version: version,
    };
  }

  function validateSnapshot(snapshot) {
    if (!snapshot || Number(snapshot.schema_version) !== 1) {
      throw new ExportError(`Unsupported Zotero snapshot schema: ${snapshot?.schema_version ?? "missing"}`);
    }
    if (!Array.isArray(snapshot.collections)
        || !snapshot.attachments || typeof snapshot.attachments !== "object"
        || !snapshot.annotations || typeof snapshot.annotations !== "object"
        || !snapshot.notes || typeof snapshot.notes !== "object"
        || !String(snapshot.data_dir || "").trim()) {
      throw new ExportError("Invalid Zotero snapshot: required export data is missing");
    }
    const collectionIDs = new Set();
    const collectionKeys = new Set();
    for (const collection of snapshot.collections) {
      if (!Number.isInteger(collection?.id)
          || !String(collection?.key || "").trim()
          || !String(collection?.name || "").trim()
          || collectionIDs.has(collection.id)
          || collectionKeys.has(collection.key)) {
        throw new ExportError("Invalid Zotero snapshot: collection identifiers must be unique");
      }
      collectionIDs.add(collection.id);
      collectionKeys.add(collection.key);
      const attachments = snapshot.attachments[String(collection.id)] || [];
      if (!Array.isArray(attachments)) {
        throw new ExportError(`Invalid Zotero snapshot attachments for collection ${collection.key}`);
      }
      for (const attachment of attachments) {
        if (!Number.isInteger(attachment?.attachment_id)
            || !String(attachment?.attachment_key || "").trim()
            || !String(attachment?.item_key || "").trim()) {
          throw new ExportError(`Invalid Zotero attachment in collection ${collection.key}`);
        }
      }
    }
    return snapshot;
  }

  class Exporter {
    constructor(snapshot, fileSystem, options) {
      this.snapshot = snapshot;
      this.fs = fileSystem;
      this.outputDir = options.outputDir;
      this.recursive = options.recursive !== false;
      this.includeNonPdf = Boolean(options.includeNonPdf);
      this.exportAnnotations = Boolean(options.exportAnnotations);
      this.exportMetadata = options.exportMetadata !== false;
      this.annotationLayout = validateAnnotationLayout(options.annotationLayout);
      this.filenameTemplate = validateFilenameTemplate(options.filenameTemplate);
      this.overwrite = Boolean(options.overwrite);
    }

    absolute(workspace, relative = "") {
      return relativeParts(relative).reduce((path, part) => this.fs.join(path, part), workspace);
    }

    async ensureSafeDestination(workspace, relative) {
      if (!isSafeRelative(relative)) {
        throw new ExportError(`Unsafe workspace path: ${relative}`);
      }
      if (!this.fs.realPath || !this.fs.isWithin || !await this.fs.exists(workspace)) return;
      const realWorkspace = await this.fs.realPath(workspace);
      const realOutput = await this.fs.realPath(this.outputDir);
      if (!await this.fs.isWithin(realWorkspace, realOutput)) {
        throw new ExportError(`Workspace resolves outside the output directory: ${workspace}`);
      }
      let cursor = workspace;
      for (const part of relativeParts(parentRelative(relative))) {
        cursor = this.fs.join(cursor, part);
        if (!await this.fs.exists(cursor)) break;
        if (!await this.fs.isWithin(await this.fs.realPath(cursor), realWorkspace)) {
          throw new ExportError(`Workspace path resolves outside the workspace: ${cursor}`);
        }
      }
    }

    async loadWorkspace(workspace) {
      const currentPath = this.absolute(workspace, `${CONTROL_DIRECTORY}/${MANIFEST_FILENAME}`);
      const legacyPath = this.absolute(workspace, MANIFEST_FILENAME);
      for (const [path, legacy] of [[currentPath, false], [legacyPath, true]]) {
        if (!await this.fs.exists(path)) continue;
        try {
          const manifest = normalizeManifest(JSON.parse(await this.fs.readText(path)));
          if (manifest) return { manifest, path, legacy };
        } catch (_error) {
          return { manifest: null, path, legacy };
        }
      }
      return { manifest: null, path: currentPath, legacy: false };
    }

    collectionDirectories(rootID) {
      const collections = [...this.snapshot.collections].sort(
        (a, b) => String(a.name).localeCompare(String(b.name)) || a.id - b.id,
      );
      const result = new Map([[rootID, ""]]);
      if (!this.recursive) return result;
      const visit = (parentID, parentPath) => {
        const children = collections.filter((collection) => collection.parent_id === parentID);
        const reserved = new Set();
        for (const child of children) {
          let name = sanitizeComponent(child.name);
          if (reserved.has(name.toLowerCase())) name = `${name} [${child.key}]`;
          reserved.add(name.toLowerCase());
          const path = joinRelative(parentPath, name);
          result.set(child.id, path);
          visit(child.id, path);
        }
      };
      visit(rootID, "");
      return result;
    }

    gather(root) {
      const directories = this.collectionDirectories(root.id);
      const collections = new Map(this.snapshot.collections.map((value) => [value.id, value]));
      const gathered = [];
      for (const [collectionID, directory] of directories) {
        const collection = collections.get(collectionID);
        for (const attachment of this.snapshot.attachments[String(collectionID)] || []) {
          if (this.includeNonPdf || isPdf(attachment)) {
            gathered.push({ collection, directory, attachment });
          }
        }
      }
      return gathered;
    }

    async workspaceFor(root) {
      let workspace = this.fs.join(this.outputDir, sanitizeComponent(root.name));
      let loaded = await this.loadWorkspace(workspace);
      if (loaded.manifest && loaded.manifest.collection_key !== root.key) {
        workspace = this.fs.join(this.outputDir, `${sanitizeComponent(root.name)} [${root.key}]`);
        loaded = await this.loadWorkspace(workspace);
      }
      return { workspace, loaded };
    }

    async reconcile(workspace, gathered, previous, currentIdentities) {
      const consumed = new Set();
      let reconciled = 0;
      for (const item of gathered) {
        const id = identity(item.collection.key, item.attachment.attachment_key);
        const source = item.attachment.source_path;
        if (previous.has(id) || !source || !await this.fs.isFile(source)) continue;
        const sourceDigest = await this.fs.hash(source);
        for (const [oldID, candidate] of previous) {
          if (currentIdentities.has(oldID) || consumed.has(oldID)) continue;
          if (candidate.collection_key !== item.collection.key) continue;
          if (!isSafeRelative(candidate.destination_path)) continue;
          const destination = this.absolute(workspace, candidate.destination_path);
          if (await this.fs.isFile(destination) && await this.fs.hash(destination) === sourceDigest) {
            previous.set(id, {
              ...candidate,
              item_key: item.attachment.item_key,
              attachment_key: item.attachment.attachment_key,
              source_path: source,
              source_sha256: sourceDigest,
              state: "active",
            });
            consumed.add(oldID);
            reconciled += 1;
            break;
          }
        }
      }
      return { consumed, reconciled };
    }

    async assignDestinations(workspace, gathered, previous, ignoredExistingFiles = new Set()) {
      const assignments = new Map();
      const reserved = new Map();
      const reservedBundles = new Map();
      const names = (map, directory) => {
        if (!map.has(directory)) map.set(directory, new Set());
        return map.get(directory);
      };

      for (const item of gathered) {
        const id = identity(item.collection.key, item.attachment.attachment_key);
        const prior = previous.get(id);
        if (!prior || !isSafeRelative(prior.destination_path)) continue;
        const expected = item.directory;
        const actualParent = parentRelative(prior.destination_path);
        const valid = this.annotationLayout === "bundle"
          ? parentRelative(actualParent) === expected
          : actualParent === expected;
        if (!valid) continue;
        assignments.set(id, prior.destination_path);
        names(reserved, actualParent).add(basename(prior.destination_path).toLowerCase());
        if (this.annotationLayout === "bundle") {
          names(reservedBundles, expected).add(basename(actualParent).toLowerCase());
        }
      }

      if (await this.fs.exists(workspace) && !this.overwrite) {
        for (const relative of await this.fs.listFiles(workspace)) {
          if (relativeParts(relative)[0] === CONTROL_DIRECTORY) continue;
          if (ignoredExistingFiles.has(relative)) continue;
          names(reserved, parentRelative(relative)).add(basename(relative).toLowerCase());
        }
        if (this.annotationLayout === "bundle" && this.fs.listDirectories) {
          for (const relative of await this.fs.listDirectories(workspace)) {
            if (relativeParts(relative)[0] === CONTROL_DIRECTORY) continue;
            names(reservedBundles, parentRelative(relative)).add(basename(relative).toLowerCase());
          }
        }
      }

      for (const item of gathered) {
        const id = identity(item.collection.key, item.attachment.attachment_key);
        if (assignments.has(id)) continue;
        const desired = attachmentFilename(item.attachment, this.filenameTemplate);
        if (this.annotationLayout === "bundle") {
          const bundle = chooseAvailableName(
            stem(desired),
            names(reservedBundles, item.directory),
            item.attachment.attachment_key,
          );
          assignments.set(id, joinRelative(item.directory, bundle, `${bundle}${extension(desired)}`));
        } else {
          const name = chooseAvailableName(
            desired,
            names(reserved, item.directory),
            item.attachment.attachment_key,
          );
          assignments.set(id, joinRelative(item.directory, name));
        }
      }
      return assignments;
    }

    annotationPath(filePath, reserved) {
      let directory;
      let desired;
      if (this.annotationLayout === "separate") {
        directory = joinRelative("Annotations", parentRelative(filePath));
        desired = `${stem(filePath)}.md`;
      } else if (this.annotationLayout === "sidecar") {
        directory = parentRelative(filePath);
        desired = `${stem(filePath)}.annotations.md`;
      } else {
        directory = parentRelative(filePath);
        desired = "annotations.md";
      }
      if (!reserved.has(directory)) reserved.set(directory, new Set());
      return joinRelative(directory, chooseAvailableName(desired, reserved.get(directory)));
    }

    childNotes(attachment) {
      const notes = [];
      const seen = new Set();
      for (const parentID of [attachment.item_id, attachment.attachment_id]) {
        for (const note of this.snapshot.notes[String(parentID)] || []) {
          if (!seen.has(note.note_id)) {
            notes.push(note);
            seen.add(note.note_id);
          }
        }
      }
      return notes;
    }

    renderAnnotation(collection, attachment, filePath, annotationPath) {
      const annotations = this.snapshot.annotations[String(attachment.attachment_id)] || [];
      const notes = this.childNotes(attachment);
      const title = attachment.title || stem(filePath);
      const fileLink = quotePath(relativeLink(parentRelative(annotationPath), filePath));
      const lines = [
        GENERATED_MARKER, "", `# ${title}`, "",
        `- Zotero item key: \`${attachment.item_key}\``,
        `- Zotero attachment key: \`${attachment.attachment_key}\``,
      ];
      if (attachment.creators?.length) lines.push(`- Creators: ${attachment.creators.join(", ")}`);
      const year = extractYear(attachment.date);
      if (year) lines.push(`- Year: ${year}`);
      if (attachment.doi) lines.push(`- DOI: ${attachment.doi}`);
      if (attachment.tags?.length) lines.push(`- Item tags: ${attachment.tags.join(", ")}`);
      lines.push(`- Exported file: [${basename(filePath)}](${fileLink})`);
      if (collection.library_id === 1) {
        lines.push(`- Open in Zotero: [PDF](zotero://open-pdf/library/items/${attachment.attachment_key})`);
      }
      lines.push("", "## Annotations", "");
      if (!annotations.length) lines.push("_No PDF annotations found._", "");
      const images = [];
      const assetDirectory = `${stem(annotationPath)}.assets`;
      for (const annotation of annotations) {
        const location = annotation.page_label ? `Page ${annotation.page_label}` : "Unnumbered page";
        const type = String(annotation.annotation_type || "annotation");
        lines.push(`### ${location} — ${type.charAt(0).toUpperCase()}${type.slice(1)}`, "");
        if (annotation.color) lines.push(`- Color: \`${annotation.color}\``);
        if (annotation.author_name) lines.push(`- Author: ${annotation.author_name}`);
        if (annotation.tags?.length) lines.push(`- Tags: ${annotation.tags.join(", ")}`);
        lines.push(`- Modified: ${annotation.date_modified || ""}`);
        if (annotation.text) {
          lines.push("", ...String(annotation.text).trim().split(/\r?\n/).map((line) => line ? `> ${line}` : ">"));
        }
        if (annotation.comment) lines.push("", "**Comment**", "", String(annotation.comment).trim());
        if (annotation.image_path) {
          const safeKey = sanitizeComponent(annotation.annotation_key, `annotation-${annotation.annotation_id}`, 80);
          const imageName = `${safeKey}.png`;
          const relativePath = joinRelative(parentRelative(annotationPath), assetDirectory, imageName);
          images.push({ sourcePath: annotation.image_path, relativePath });
          lines.push("", `![${location} ${type} annotation](${quotePath(joinRelative(assetDirectory, imageName))})`);
        } else if (["image", "ink"].includes(type)) {
          lines.push("", "_Image preview is unavailable in Zotero's local cache._");
        }
        if (collection.library_id === 1) {
          lines.push("", `[Open annotation in Zotero](zotero://open-pdf/library/items/${attachment.attachment_key}?annotation=${annotation.annotation_key})`);
        }
        lines.push("");
      }
      lines.push("## Child notes", "");
      if (!notes.length) lines.push("_No child notes found._", "");
      notes.forEach((note, index) => {
        lines.push(`### ${note.title || `Note ${index + 1}`}`, "", `- Modified: ${note.date_modified || ""}`);
        if (note.tags?.length) lines.push(`- Tags: ${note.tags.join(", ")}`);
        lines.push("", noteHtmlToMarkdown(note.content) || "_Empty note._", "");
      });
      return {
        relativePath: annotationPath,
        content: `${lines.join("\n").replace(/\s+$/g, "")}\n`,
        annotations: annotations.length,
        notes: notes.length,
        images,
      };
    }

    async writeAnnotations(workspace, gathered, assignments) {
      if (!this.exportAnnotations) return { files: 0, annotations: 0, notes: 0 };
      const documents = [];
      const reserved = new Map();
      for (const item of gathered) {
        if (!isPdf(item.attachment)) continue;
        const id = identity(item.collection.key, item.attachment.attachment_key);
        const filePath = assignments.get(id);
        const annotationPath = this.annotationPath(filePath, reserved);
        documents.push(this.renderAnnotation(
          item.collection, item.attachment, filePath, annotationPath,
        ));
      }
      const targets = new Set(documents.map((document) => document.relativePath));
      for (const document of documents) {
        for (const image of document.images) {
          const cache = this.fs.join(this.snapshot.data_dir, "cache");
          let sourceBoundary = image.sourcePath;
          let cacheBoundary = cache;
          if (this.fs.realPath && await this.fs.exists(cache) && await this.fs.exists(image.sourcePath)) {
            sourceBoundary = await this.fs.realPath(image.sourcePath);
            cacheBoundary = await this.fs.realPath(cache);
          }
          if (!String(image.sourcePath).toLowerCase().endsWith(".png")
              || !await this.fs.isFile(image.sourcePath)
              || (this.fs.isWithin && !await this.fs.isWithin(sourceBoundary, cacheBoundary))) {
            throw new ExportError(`Annotation image is not a readable Zotero cache PNG: ${image.sourcePath}`);
          }
        }
      }
      for (const document of documents) {
        await this.ensureSafeDestination(workspace, document.relativePath);
        const target = this.absolute(workspace, document.relativePath);
        if (await this.fs.exists(target)) {
          const existing = await this.fs.readText(target);
          if (!existing.startsWith(GENERATED_MARKER)) {
            throw new ExportError(`Refusing to replace unmanaged annotation file: ${target}`);
          }
          if (existing !== document.content) await this.fs.writeTextAtomic(target, document.content);
        } else {
          await this.fs.writeTextAtomic(target, document.content);
        }
        for (const image of document.images) {
          await this.ensureSafeDestination(workspace, image.relativePath);
          const destination = this.absolute(workspace, image.relativePath);
          const directory = this.absolute(workspace, parentRelative(image.relativePath));
          const marker = this.fs.join(directory, ".zpm-generated");
          if (await this.fs.exists(directory) && !await this.fs.exists(marker)) {
            throw new ExportError(`Refusing unmanaged annotation asset directory: ${directory}`);
          }
          if (!await this.fs.exists(marker)) await this.fs.writeTextAtomic(marker, ASSET_MARKER);
          if (!await this.fs.isFile(destination)
              || await this.fs.hash(destination) !== await this.fs.hash(image.sourcePath)) {
            await this.fs.copyAtomic(image.sourcePath, destination);
          }
        }
      }
      if (await this.fs.exists(workspace)) {
        for (const relative of await this.fs.listFiles(workspace)) {
          if (!relative.toLowerCase().endsWith(".md") || targets.has(relative)) continue;
          const path = this.absolute(workspace, relative);
          if ((await this.fs.readText(path)).startsWith(GENERATED_MARKER)
              && relativeParts(relative)[0] !== CONTROL_DIRECTORY) {
            await this.fs.removeFile(path);
          }
        }
      }
      return {
        files: documents.length,
        annotations: documents.reduce((sum, document) => sum + document.annotations, 0),
        notes: documents.reduce((sum, document) => sum + document.notes, 0),
      };
    }

    metadataEntry(item, entry) {
      return {
        collection_key: item.collection.key,
        item_key: item.attachment.item_key,
        attachment_key: item.attachment.attachment_key,
        title: item.attachment.title,
        date: item.attachment.date,
        creators: item.attachment.creators || [],
        doi: item.attachment.doi,
        tags: item.attachment.tags || [],
        content_type: item.attachment.content_type,
        source_path: entry.source_path,
        destination_path: entry.destination_path,
        state: entry.state,
        sha256: entry.source_sha256,
      };
    }

    renderIndex(manifest, metadata) {
      const lines = [
        GENERATED_MARKER, "", `# ${manifest.collection_name}`, "",
        `Generated from Zotero collection \`${manifest.collection_key}\` at ${manifest.exported_at}.`,
        "", "| Title | Creators | Year | DOI | Tags | File |",
        "| --- | --- | --- | --- | --- | --- |",
      ];
      const cell = (value) => String(value || "").replaceAll("|", "\\|").replaceAll("\n", " ").trim();
      for (const entry of metadata) {
        const title = cell(entry.title || stem(entry.destination_path));
        const creators = cell(entry.creators.join(", ") || "—");
        const year = extractYear(entry.date) || "—";
        const doi = entry.doi
          ? `[${cell(entry.doi)}](https://doi.org/${quotePath(entry.doi)})`
          : "—";
        const tags = cell(entry.tags.join(", ") || "—");
        const label = extension(entry.destination_path).slice(1).toUpperCase() || "File";
        const file = entry.state === "active"
          ? `[${label}](${quotePath(`../${entry.destination_path}`)})`
          : `_${cell(entry.state)}_`;
        lines.push(`| ${title} | ${creators} | ${year} | ${doi} | ${tags} | ${file} |`);
      }
      return `${lines.join("\n")}\n`;
    }

    async removeLegacyControlFiles(workspace, loaded, metadataMigrated) {
      if (loaded.legacy) await this.fs.removeFile(this.absolute(workspace, MANIFEST_FILENAME));
      const summary = this.absolute(workspace, "README.md");
      if (await this.fs.exists(summary)
          && (await this.fs.readText(summary)).startsWith("# Project exported from Zotero\n")) {
        await this.fs.removeFile(summary);
      }
      if (!metadataMigrated) return;
      const metadata = this.absolute(workspace, "metadata.json");
      if (await this.fs.exists(metadata)) {
        try {
          if (JSON.parse(await this.fs.readText(metadata)).generated_by === "zpm") {
            await this.fs.removeFile(metadata);
          }
        } catch (_error) {}
      }
      const index = this.absolute(workspace, "INDEX.md");
      if (await this.fs.exists(index) && (await this.fs.readText(index)).startsWith(GENERATED_MARKER)) {
        await this.fs.removeFile(index);
      }
    }

    async legacyGeneratedControlFiles(workspace, loaded) {
      const generated = new Set();
      if (loaded.legacy) generated.add(MANIFEST_FILENAME);
      const summary = this.absolute(workspace, "README.md");
      if (await this.fs.exists(summary)
          && (await this.fs.readText(summary)).startsWith("# Project exported from Zotero\n")) {
        generated.add("README.md");
      }
      if (!this.exportMetadata) return generated;
      const metadata = this.absolute(workspace, "metadata.json");
      if (await this.fs.exists(metadata)) {
        try {
          if (JSON.parse(await this.fs.readText(metadata)).generated_by === "zpm") {
            generated.add("metadata.json");
          }
        } catch (_error) {}
      }
      const index = this.absolute(workspace, "INDEX.md");
      if (await this.fs.exists(index) && (await this.fs.readText(index)).startsWith(GENERATED_MARKER)) {
        generated.add("INDEX.md");
      }
      return generated;
    }

    async validateControlTargets(workspace) {
      if (!this.exportMetadata) return;
      const metadata = this.absolute(workspace, `${CONTROL_DIRECTORY}/metadata.json`);
      if (await this.fs.exists(metadata)) {
        try {
          const payload = JSON.parse(await this.fs.readText(metadata));
          if (payload.generated_by !== "zpm") throw new Error("not generated by zpm");
        } catch (_error) {
          throw new ExportError(`Refusing to replace unmanaged metadata file: ${metadata}`);
        }
      }
      const index = this.absolute(workspace, `${CONTROL_DIRECTORY}/INDEX.md`);
      if (await this.fs.exists(index)
          && !(await this.fs.readText(index)).startsWith(GENERATED_MARKER)) {
        throw new ExportError(`Refusing to replace unmanaged index file: ${index}`);
      }
    }

    async export(collectionKey) {
      const root = this.snapshot.collections.find((collection) => collection.key === collectionKey);
      if (!root) throw new ExportError(`Collection not found in Zotero snapshot: ${collectionKey}`);
      const { workspace, loaded } = await this.workspaceFor(root);
      if (await this.fs.exists(workspace) && !loaded.manifest && !this.overwrite) {
        throw new ExportError(`Destination exists but is not managed by zpm: ${workspace}`);
      }
      if (loaded.manifest?.collection_key !== undefined
          && loaded.manifest.collection_key !== root.key) {
        throw new ExportError(`Workspace belongs to another Zotero collection: ${workspace}`);
      }
      const retainedSettings = [];
      if (loaded.manifest && loaded.manifest.filename_template !== this.filenameTemplate) {
        this.filenameTemplate = loaded.manifest.filename_template;
        retainedSettings.push(`filename order: ${this.filenameTemplate}`);
      }
      if (loaded.manifest && loaded.manifest.annotation_layout !== this.annotationLayout) {
        this.annotationLayout = loaded.manifest.annotation_layout;
        retainedSettings.push(`layout: ${this.annotationLayout}`);
      }
      await this.ensureSafeDestination(workspace, `${CONTROL_DIRECTORY}/${MANIFEST_FILENAME}`);
      let outputBoundary = this.outputDir;
      let dataBoundary = this.snapshot.data_dir;
      if (this.fs.realPath
          && await this.fs.exists(this.outputDir)
          && await this.fs.exists(this.snapshot.data_dir)) {
        outputBoundary = await this.fs.realPath(this.outputDir);
        dataBoundary = await this.fs.realPath(this.snapshot.data_dir);
      }
      if (this.fs.isWithin && await this.fs.isWithin(outputBoundary, dataBoundary)) {
        throw new ExportError(`Refusing to export inside the Zotero data directory: ${this.outputDir}`);
      }
      await this.validateControlTargets(workspace);

      const gathered = this.gather(root);
      const previous = new Map((loaded.manifest?.items || []).map(
        (entry) => [identity(entry.collection_key, entry.attachment_key), entry],
      ));
      const currentIdentities = new Set(gathered.map(
        (item) => identity(item.collection.key, item.attachment.attachment_key),
      ));
      const { consumed, reconciled } = await this.reconcile(
        workspace, gathered, previous, currentIdentities,
      );
      const legacyControlFiles = await this.legacyGeneratedControlFiles(workspace, loaded);
      const assignments = await this.assignDestinations(
        workspace, gathered, previous, legacyControlFiles,
      );
      const entries = [];
      const metadata = [];
      const stats = {
        collectionName: root.name, workspace, discovered: gathered.length,
        copied: 0, updated: 0, unchanged: 0, missing: 0,
        removed: 0, reconciled, retainedSettings,
      };

      for (const item of gathered) {
        const id = identity(item.collection.key, item.attachment.attachment_key);
        const prior = previous.get(id);
        const relativeDestination = assignments.get(id);
        await this.ensureSafeDestination(workspace, relativeDestination);
        const destination = this.absolute(workspace, relativeDestination);
        const source = item.attachment.source_path;
        let entry;
        if (!source || !await this.fs.isFile(source)) {
          stats.missing += 1;
          entry = {
            ...(prior || {}), collection_key: item.collection.key,
            item_key: item.attachment.item_key,
            attachment_key: item.attachment.attachment_key,
            source_path: source || item.attachment.original_path || "",
            destination_path: relativeDestination,
            source_size: prior?.source_size || 0,
            source_mtime_ns: prior?.source_mtime_ns || 0,
            source_sha256: prior?.source_sha256 || null,
            destination_size: prior?.destination_size || 0,
            destination_mtime_ns: prior?.destination_mtime_ns || 0,
            state: "missing",
          };
        } else {
          const sourceStat = await this.fs.stat(source);
          const digest = await this.fs.hash(source);
          const destinationExists = await this.fs.isFile(destination);
          const matches = destinationExists && await this.fs.hash(destination) === digest;
          if (matches) stats.unchanged += 1;
          else {
            if (destinationExists) stats.updated += 1;
            else stats.copied += 1;
            await this.fs.copyAtomic(source, destination);
          }
          const destinationStat = await this.fs.stat(destination);
          entry = {
            collection_key: item.collection.key,
            item_key: item.attachment.item_key,
            attachment_key: item.attachment.attachment_key,
            source_path: source,
            destination_path: relativeDestination,
            source_size: sourceStat.size,
            source_mtime_ns: Math.trunc(sourceStat.mtimeMs * 1_000_000),
            source_sha256: digest,
            destination_size: destinationStat.size,
            destination_mtime_ns: Math.trunc(destinationStat.mtimeMs * 1_000_000),
            state: "active",
          };
        }
        entries.push(entry);
        metadata.push(this.metadataEntry(item, entry));
      }

      for (const [id, prior] of previous) {
        if (currentIdentities.has(id) || consumed.has(id)) continue;
        stats.removed += 1;
        entries.push({ ...prior, state: "removed" });
      }

      const manifest = {
        version: MANIFEST_VERSION,
        exported_at: isoTimestamp(),
        collection_key: root.key,
        collection_name: root.name,
        filename_template: this.filenameTemplate,
        annotation_layout: this.annotationLayout,
        items: entries,
      };
      const control = this.absolute(workspace, CONTROL_DIRECTORY);
      await this.ensureSafeDestination(workspace, `${CONTROL_DIRECTORY}/${MANIFEST_FILENAME}`);
      await this.fs.makeDir(control);
      await this.fs.writeTextAtomic(
        this.fs.join(control, MANIFEST_FILENAME), `${JSON.stringify(manifest, null, 2)}\n`,
      );
      const pdfCount = entries.filter(
        (entry) => entry.state === "active" && extension(entry.destination_path).toLowerCase() === ".pdf",
      ).length;
      await this.fs.writeTextAtomic(
        this.fs.join(control, "export-summary.md"),
        `# Project exported from Zotero\n\nCollection: ${root.name}\n\n`
          + `Collection key: ${root.key}\n\nExported: ${manifest.exported_at.slice(0, 10)}\n\n`
          + `PDF count: ${pdfCount}\n\nMissing attachments: ${stats.missing}\n\n`
          + `Removed attachments retained: ${stats.removed}\n`,
      );
      if (this.exportMetadata) {
        const payload = {
          generated_by: "zpm", schema_version: 1, generated_at: manifest.exported_at,
          collection_key: root.key, collection_name: root.name,
          items: metadata.sort((a, b) => a.destination_path.localeCompare(b.destination_path)),
        };
        await this.fs.writeTextAtomic(
          this.fs.join(control, "metadata.json"), `${JSON.stringify(payload, null, 2)}\n`,
        );
        await this.fs.writeTextAtomic(
          this.fs.join(control, "INDEX.md"), this.renderIndex(manifest, payload.items),
        );
      }
      const annotationStats = await this.writeAnnotations(workspace, gathered, assignments);
      Object.assign(stats, {
        annotationFiles: annotationStats.files,
        annotations: annotationStats.annotations,
        notes: annotationStats.notes,
      });
      await this.removeLegacyControlFiles(workspace, loaded, this.exportMetadata);
      return stats;
    }
  }

  async function exportSnapshot(snapshot, fileSystem, collectionKey, options) {
    validateSnapshot(snapshot);
    if (!fileSystem || !String(options?.outputDir || "").trim()) {
      throw new ExportError("An export folder is required");
    }
    return new Exporter(snapshot, fileSystem, options).export(collectionKey);
  }

  const api = {
    CONTROL_DIRECTORY,
    ExportError,
    exportSnapshot,
    sanitizeComponent,
    attachmentFilename,
    noteHtmlToMarkdown,
    normalizeManifest,
    validateSnapshot,
    isPdf,
  };
  if (typeof module !== "undefined") module.exports = api;
  return api;
})();
